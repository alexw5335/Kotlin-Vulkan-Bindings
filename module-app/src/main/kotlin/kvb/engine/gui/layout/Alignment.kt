package kvb.engine.gui.layout

enum class Alignment {


	/**
	 * Elements are placed from the start.
	 */
	START,

	/**
	 * Elements are placed from the centre.
	 */
	CENTRE,

	/**
	 * Elements are placed from the end.
	 */
	END,

	/**
	 * Elements are spaced evenly with the first element at the start and the last element at the end.
	 */
	FILL;


}